package de.vzg.noa.oai;

/**
 * @author Ren\u00E9 Adler (eagle)
 *
 */
public class OAIFile {

    private String name;

    private Long size;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the size
     */
    public Long getSize() {
        return size;
    }

    /**
     * @param size the size to set
     */
    public void setSize(Long size) {
        this.size = size;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        StringBuilder builder = new StringBuilder();
        builder.append("OAIFile [");
        if (name != null) {
            builder.append("name=");
            builder.append(name);
            builder.append(", ");
        }
        if (size != null) {
            builder.append("size=");
            builder.append(size);
        }
        builder.append("]");
        return builder.toString();
    }

}
